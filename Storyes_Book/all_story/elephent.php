    <h3>Story of the Sparrow and the Elephant</h3>
    <p class="fs-5 ms-5">A long time ago, Mr and Mrs Sparrow lived in the forest. They made their nest on a branch of a
        blackberry tree. Soon, Mrs Sparrow laid eggs in the nest. Mr and Mrs Sparrow became very happy. Mr Sparrow took
        care of her and their eggs. The Sparrow couple eagerly waited for their kids to come out. <br><br>


        But one day, a big-giant and cheeky Elephant came near their tree. He started pulling the branches. The Sparrow
        couple got scared after watching this. They got afraid of the big Elephant and started requesting him. They
        pleaded, “Oh! Dear Elephant, Please show mercy. This is our beautiful nest. We laid eggs in the nest. We are
        waiting for our children to come out eagerly. Please don't break the branches or else our beautiful dreams will
        also be broken”. <br><br>


        But that selfish Elephant did not listen to anything. He continued destroying that place. At last, he broke that
        branch. This made the eggs fall and break in a blink of an eye. Mr and Mrs Sparrow started crying. </p>

    <h3>A Friend in a Need is a Friend Indeed</h3>
    <p class="fs-5 ms-5">Seeing this horrible incident, the Sparrow couple’s friends came to console them. Some friends
        were so deeply affected by this incident that they decided to punish the Elephant. They went to the ‘mastermind
        Frog’. The mastermind Frog was considered as the most intelligent animal in the jungle. After listening to this,
        the mastermind Frog decided to fight against the sniffy Elephant.</p>

    <h3>What Is the Plan?</h3>
    <p class="fs-5 ms-5">The mastermind Frog made a plan to punish the Elephant. He included Gnat and Woodpeacker into
        his plan. According to his plan, Gnat would buzz in the Elephant’s ear, and annoy him. After that, the
        Woodpecker would pluck his eyes, and finally, the Frog would complete the rest of the plan. <br><br>


        On the next day, all started to search for the arrogant Elephant. Later, they found him near the mango tree.
        “Let us execute the plan”, said the genius Frog. Following the order of the mastermind Frog, Gnat went near the
        Elephant and started buzzing into his ear. This made the Elephant angry, and soon he lost control over his body. <br><br>

        As he lost control and started to panic, the Woodpecker flew towards him and took his eyes off. Moaning in
        severe pain, the Elephant started crying. Due to intemperate pain and excessive crying, his throat started
        drying up. He went here and there in search of water, but he could not see anything.</p>

    <h3>What Was the Mastermind Frog’s Master Plan?</h3>
    <p class="fs-5 ms-5">Listening to –tarr–tarr–tarr– the sound of the Frog, the Elephant thought that he found the
        lake. He went near the ditch following the Frog’s croak. The Frog jumped into the lake. Thus, the blind Elephant
        assured himself that he had reached the correct place. As he went down to drink some water, he fell into the
        ditch and died soon after. As all friends took revenge using their intelligence, all animals and birds cheered
        their win and started dancing and singing.
    </p>
    <h3>Moral of the Story</h3>
    <p class="fs-5 ms-5">The moral of the story is that wit is always superior to brute force. Also, it teaches us that
        we should always help our friends and never let them be alone in their sorrows.</p>
    <h3>Tip for Parents</h3>
    <p class="fs-5 ms-5">We request all parents to share their friendship stories with little kids and help them to
        become good social people. </p>
